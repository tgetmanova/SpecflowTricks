namespace ElectronicReaderLibrary.Data
{
    /// <summary>
    /// Enumerates the electronic book data format
    /// </summary>
    public enum DataFormat
    {
        /// <summary>
        /// The none
        /// </summary>
        None = 0,

        /// <summary>
        /// The PDF
        /// </summary>
        Pdf = 1,

        /// <summary>
        /// The djvu
        /// </summary>
        Djvu = 2,

        /// <summary>
        /// The RTF
        /// </summary>
        Rtf = 3,

        /// <summary>
        /// The epub
        /// </summary>
        Epub = 4,

        /// <summary>
        /// The FD2
        /// </summary>
        Fb2 = 5,

        /// <summary>
        /// The text
        /// </summary>
        Txt = 6
    }
}
